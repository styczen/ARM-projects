void HAL_UART_RxCpltCallback(UART_HandleTypeDef *huart) {
	uint16_t crc = 0, read_crc = 0;
	if (huart == &huart3) {
		switch (temp) {

//		switch (RX_cmd[0]) {
		case 1: //DebugSteeringSystem
			TX_cmd[0] = 1;
			TX_cmd[1] = (uint8_t) (adc_data[0] >> 8);
			TX_cmd[2] = (uint8_t) (adc_data[0]);
			TX_cmd[3] = (uint8_t) (adc_data[1] >> 8);
			TX_cmd[4] = (uint8_t) (adc_data[1]);
			crc = crcFast(TX_cmd, 5);
			TX_cmd[5] = (uint8_t) (crc >> 8);
			TX_cmd[6] = (uint8_t) (crc);
			HAL_UART_Transmit_IT(&huart3, TX_cmd, 7);
			break;
		case 2: //DebugSpeed
			TX_cmd[0] = 2;
			TX_cmd[1] = (uint8_t) (adc_data[0] >> 8);
			TX_cmd[2] = (uint8_t) (adc_data[0]);
			TX_cmd[3] = (uint8_t) (adc_data[1] >> 8);
			TX_cmd[4] = (uint8_t) (adc_data[1]);
			crc = crcFast(TX_cmd, 5);
			TX_cmd[5] = (uint8_t) (crc >> 8);
			TX_cmd[6] = (uint8_t) (crc);
			HAL_UART_Transmit_IT(&huart3, TX_cmd, 7);
			break;
		case 3: //DebugEncodersBrakesIndicators
			TX_cmd[0] = 3;
			TX_cmd[1] = (uint8_t) (adc_data[0] >> 8);
			TX_cmd[2] = (uint8_t) (adc_data[0]);
			TX_cmd[3] = (uint8_t) (adc_data[1] >> 8);
			TX_cmd[4] = (uint8_t) (adc_data[1]);
			TX_cmd[5] = (uint8_t) (adc_data[0] & 0b00000011);
			crc = crcFast(TX_cmd, 6);
			TX_cmd[6] = (uint8_t) (crc >> 8);
			TX_cmd[7] = (uint8_t) (crc);
			HAL_UART_Transmit_IT(&huart3, TX_cmd, 8);
			break;
		case 4:
			// receive control from ROS
			// CRC check, if data not valid reject
			read_crc = (RX_cmd[8] << 8) + RX_cmd[9];
			if (crcFast(RX_cmd, 9) == read_crc) {
				HAL_GPIO_WritePin(LD2_GPIO_Port, LD2_Pin, GPIO_PIN_SET);
//				steering_wheel = RX_cmd[1] + (RX_cmd[2] << 8);
//				speed = RX_cmd[3] + (RX_cmd[4] << 8);
//				brake_value = RX_cmd[5] + (RX_cmd[6] << 8);
//				gas_cut_off = RX_cmd[7] >> 0;
//				dir = RX_cmd[7] >> 1;
			} else
				HAL_GPIO_WritePin(LD2_GPIO_Port, LD2_Pin, GPIO_PIN_RESET);
			break;
		default:
			break;
		}
//		HAL_UART_Receive_IT(&huart3, RX_cmd, 10);
		HAL_UART_Receive_IT(&huart3, &temp, 1);

	}
}

